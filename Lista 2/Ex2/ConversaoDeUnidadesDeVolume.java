
public class ConversaoDeUnidadesDeVolume {
	

	public static double centimetrosCubicos(double volume) {
		return volume * 1000;
	}

	public static double litros(double volume) {
		return volume * 1000;
	}

	public static double pesCubicos(double volume) {
		return volume * 35.32;
	}

	public static double polegadasCubicas(double volume) {
		return volume * 231;
	}

	public static double galoesLitros(double volume) {
		return volume * 3.785;
	}
}
