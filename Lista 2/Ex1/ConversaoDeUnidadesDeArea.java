

public class ConversaoDeUnidadesDeArea
{
	
	
	public static double pesQuadrados(double area)
	{
		return area * 10.76;	
	}
	public static double centimetrosQuadrados(double area)
	{
		return area * 929;
	}
	public static double acresQuadrados(double area)
	{
		return area * 640;	
	}
	public static double pesQuadradosAcres(double area)
	{
		return area * 43560;
	}
	
}
